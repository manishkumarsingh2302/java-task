class ThreedArray2 {
	public static void main (String args[])
	{
	int[][][]a={{{584,487,547,7458},{457,478,5},{7845,5445,5144,57425}}};
	 for(int i=0;i<a.length;i++)
	{
	for( int j=0;j<a[i].length;j++)
	{
	for( int k=0;k<a[i][j].length;k++)
	{
	System.out.print(a[i][j][k]+" ");

	}
	System.out.println();


	}
   
	}


	}


}